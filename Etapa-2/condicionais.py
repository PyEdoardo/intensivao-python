idade = 0

if idade < 18:
    print(f"Você é menor de Idade!\nIdade: {idade}") ##O If representa um "Se", ou seja, caso uma condição seja verdadeira, ele irá executar o código abaixo!
else:
    print(f"Você é maior de Idade!\nIdade: {idade}") ##O Else representa um "Senão", nesse caso, se as expressões acima não forem verdadeiras, ele irá ser executado.