##F String:

nome = "Edoardo"
print(f"Meu nome é: {nome}") ##Usando o F antes das "", podemos usar chaves para colocar uma variável dentro do print, sem precisar usar virgula para adicionar na direita!

print(f"Meu nome é: \n{nome}") ##Usando o \n, podemos usar o famoso breakline, assim na saída do print, oque estiver a direita do \n irá para a linha debaixo!

print(f"Multiplicação: {8 * 8}") ##Podemos fazer qualquer tipo de cálculo simples dentro das chaves, o limite é sua criatividade!