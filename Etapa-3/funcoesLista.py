pessoas = ["Ciclano", "Beutrano", "Fulano", "Entano"]

print(f"Elemento a ser removido: {pessoas.pop()}") ##Esse print irá remover o último elemento, e depois retornar esse elemento para um print!

print(f"Elemento índice 1: {pessoas.pop(1)}") ##Esse print irá remover o item Beutrano, pois está 2 posição...

print(f"\nLista depois dos pops: \n{pessoas}") ##Como removemos 2 elementos, esse print irá retornar a lista resultante das remoções!

